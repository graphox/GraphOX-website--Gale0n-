<h1>About</h1>

<p>GraphOX is a Sauerbraten (Cube 2) mod that allows the user to switch between themes and use additional features. 
    It was founded by Aaron (AKA :w:w:|gear4) almost immediately helped by a_teammate (|OSH|Ade) and killme (Fan|killme_nl). 
    The project also involved a server mod named AlphaServ which is still actively developed by killme, 
    just like the web administration part of this mod. gear4 created the site on a different custom made CMS 
    to troll killme (:P). The developers of GraphOX also helped with the development of QClient, 
    created by Gleuwuzah, which is something like Steam for Sauerbraten. 
    It uses the AlphaServ control panel API written by killme. 
    While gear4 implemented the basic theme system, killme made a SVN downloader inspired by SauerBomber 
    to be able to download updates and packages from the GraphOX repository, which would allow automatic 
    map updating.</p>